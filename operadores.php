<?php


$valor1 = 10;
$valor2 = 20;

echo "Valor1: $valor1<br>Valor2: $valor2<br>";

//soma
$resultado = $valor1 + $valor2;
echo "Resultado da soma: $resultado<br>";

//subtração
$resultado = $valor1 - $valor2;
echo "Resultado da subtração: $resultado<br>";

//multiplicação
$resultado = $valor1 * $valor2;
echo "Resultado da multiplicação: $resultado<br>";

//divisão
$resultado = $valor1 / $valor2;
echo "Resultado da divisão: $resultado<br>";

//módulo (resto da divisão)
$resultado = $valor1 % $valor2;
echo "Resultado da módulo: $resultado<br>";

$resultado = $valor1 * 2 + $valor2;
echo "Resultado do cálculo: $resultado<br>";

//precedencia
$resultado = $valor1 + (($valor1 + 2) * $valor2);
echo "Resultado do cálculo: $resultado<br>";

